var SelectedColors: Array<string> = [];
var Count: number = 5;
var colorMain: string = "";

window.onload = function (event: any): void {
    setSelectedColor();
    startGame();
}

function startGame(): void {
    let correct = false;

    while (!correct && Count != 0) {
        let answer: string | null = prompt(`Qual é a cor ? \nVidas: ${Count}  \nCores: ${SelectedColors.join(', ')}`);
        correct = checkAnswer(answer ? answer.toLowerCase() : "");
        console.log(colorMain);


        if (!correct) {
            Count--;
            alert(`Incorreto. Tente novamente`)
        }
    }


    if (Count == 0)
        alert("Infelizmente você perdeu! Pressione F5 para recomeçar.");
    else {
        alert("Parabéns, você ganhou");
        document.getElementsByTagName("body")[0].style.backgroundColor = colorMain;
    }
}

function setSelectedColor(): void {
    SelectedColors = [];
    for (let i = 0; i < 10; i++) {
        let index = Math.floor(Math.random() * Colors.length);
        SelectedColors.push(Colors[index]);
    }

    colorMain = SelectedColors[Math.floor(Math.random() * SelectedColors.length)].toLowerCase();
}

function resetGame(): void {
    Count = 5;
    setSelectedColor();
    startGame();
}

function checkAnswer(text: string): boolean {
    return colorMain === text;
}

function isLower(text: string): boolean {
    return text < colorMain;
}